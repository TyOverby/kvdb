open! Core
open Common
module Table = Int_to_string

let%expect_test "empty table" =
  let t = Table.create ~path:"foo" in
  print_endline (Table.to_string_for_testing t);
  [%expect
    {|
    ┌─────┬──────┬─────────────┬──────────────┐
    │ key │ data │ key (bytes) │ data (bytes) │
    ├┬┬┬┬┬┼┬┬┬┬┬┬┼┬┬┬┬┬┬┬┬┬┬┬┬┬┼┬┬┬┬┬┬┬┬┬┬┬┬┬┬┤
    └┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┴┘
    |}]
;;

